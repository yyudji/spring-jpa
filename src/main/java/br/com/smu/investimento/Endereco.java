package br.com.smu.investimento;

import javax.persistence.CascadeType;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.OneToOne;
import javax.persistence.SequenceGenerator;

@Entity
@SequenceGenerator(name="endereco", sequenceName = "SQ_ENDERECO", allocationSize = 1)
public class Endereco {
	
	@Id
	@GeneratedValue(generator = "id_endereco", strategy = GenerationType.SEQUENCE)
	private int id;
	private String rua;
	private String numero;
	private String cep;
	private String bairro;
	private String estado;
	private String cidade;
	
	@OneToOne(mappedBy = "endereco", cascade = CascadeType.PERSIST)
	private Investidor investidor;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRua() {
		return rua;
	}
	public void setRua(String rua) {
		this.rua = rua;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	public String getCep() {
		return cep;
	}
	public void setCep(String cep) {
		this.cep = cep;
	}
	public String getBairro() {
		return bairro;
	}
	public void setBairro(String bairro) {
		this.bairro = bairro;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
}
